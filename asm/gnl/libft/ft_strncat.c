#include "libft.h"

char	*ft_strncat(char *s2, const char *s1, size_t n)
{
	size_t	i;
	int		l;

	i = 0;
	l = 0;
	l = ft_strlen(s2);
	while (s1[i] != '\0' && i < n)
	{
		s2[l] = s1[i];
		i++;
		l++;
	}
	s2[l] = '\0';
	return (s2);
}
