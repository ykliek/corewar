#include "libft.h"

int	ft_words(char const *s, char c, int n)
{
	int	f;
	int	i;

	i = 0;
	f = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			f = 0;
		else
			f = 1;
		if (f == 1)
		{
			n++;
			while (s[i] != '\0' && s[i] != c)
				i++;
		}
		else
		{
			while (s[i] == c)
				i++;
		}
	}
	return (n);
}
