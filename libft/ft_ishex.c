#include "libft.h"

int		ft_ishex(char ch)
{
	if ((ch >= '0' && ch <= '9') || (ch >= 'a' && ch <= 'f') ||
													(ch >= 'A' && ch <= 'F'))
		return (1);
	return (0);
}
