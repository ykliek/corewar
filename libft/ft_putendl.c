#include "libft.h"

void	ft_putendl(char const *s)
{
	int count;

	count = 0;
	if (s)
	{
		while (s[count] != '\0')
		{
			ft_putchar(s[count]);
			count++;
		}
		ft_putchar('\n');
	}
}
